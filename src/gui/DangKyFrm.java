    private void checkConfirmPassword() {
        String p = new String(PasswordField.getPassword());
        String cP = new String(confirmPass.getPassword());
        if (cP.isEmpty()) {
            confirmPass.setBorder(defaultBorder);
            lbErrorConPass.setText(" ");
        } else if (!p.equals(cP)) {
            confirmPass.setBorder(javax.swing.BorderFactory.createLineBorder(java.awt.Color.RED, 2));
            lbErrorConPass.setText("* X√°c nh·∫≠n m·∫≠t kh·∫©u kh√¥ng kh·ªõp");
        } else {
            confirmPass.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 153, 51), 2)); 
            lbErrorConPass.setText(" ");
        }
    }

    private void checkPassword() {
        String p = new String(PasswordField.getPassword());
        passStrength = 0;
        
        if (p.length() > 0) passStrength++; 
        if (p.length() >= 6) passStrength++; 
        if (p.matches(".*[a-z].*") && p.matches(".*[A-Z].*")) passStrength++; 
        if (p.matches(".*\\d.*")) passStrength++; 
        if (p.matches(".*[!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>\\/?].*")) passStrength++; 
        if (p.length() >= 8 && passStrength == 5) passStrength++; 
        
        LablePassword.setText("Password:");
        
        if (p.isEmpty()) {
            PasswordField.setBorder(defaultBorder);
            lbErrorPass.setText(" ");
        } else if (passStrength < 3) {
            PasswordField.setBorder(javax.swing.BorderFactory.createLineBorder(java.awt.Color.RED, 2));
            lbErrorPass.setText("* M·∫≠t kh·∫©u qu√° y·∫øu (C·∫ßn ch·ªØ hoa, s·ªë, k√Ω t·ª± ƒë.bi·ªát)");
        } else {
            PasswordField.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 153, 51), 2)); 
            lbErrorPass.setText(" ");
        }
        
        checkConfirmPassword(); 
    }
    
